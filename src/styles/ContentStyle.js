function rand() {
  return Math.round(Math.random() * 20) - 10;
}


export function getModalStyle() {
  const top = 35 + rand();
  const left = 50 + rand();

  return {
    top: `${top}%`,
    left: `${left}%`,
    transform: `translate(-${top}%, -${left}%)`,
  };
}


